  // * ===== Nice Select
  $('select').niceSelect();

  // * ===== Form Field
  (function formField() {
    const inputs = document.querySelectorAll('.form-submit__input');

    inputs.forEach((input) => {
      input.addEventListener('change', () => {
        if (input.value.length !== 0) {
          input.classList.add('focused');
        } else {
          input.classList.remove('focused');
        }
      });
    });
  })();

  // * ===== Show Hide Password
